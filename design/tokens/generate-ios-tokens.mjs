// Generates Swift token files for DesignSystem from fish.tokens.json.
// OKLCH -> sRGB uses Björn Ottosson's published OKLab matrices. Conversion
// happens offline so the app ships plain sRGB dynamic colors.
import {
  existsSync,
  mkdirSync,
  readFileSync,
  writeFileSync,
} from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const MANIFEST = path.join(HERE, "fish.tokens.json");
const OUT_DIR = path.join(
  HERE,
  "..",
  "..",
  "apps",
  "ios",
  "FishKit",
  "Sources",
  "DesignSystem",
  "Generated",
);

export function oklchToSrgb(value) {
  const [l, c, h = 0, alpha = 1] = Array.isArray(value)
    ? value
    : [value.l, value.c, value.h ?? 0, value.alpha ?? 1];
  const hr = h * Math.PI / 180;
  const a = c * Math.cos(hr);
  const b = c * Math.sin(hr);
  const lPrime = l + 0.3963377774 * a + 0.2158037573 * b;
  const mPrime = l - 0.1055613458 * a - 0.0638541728 * b;
  const sPrime = l - 0.0894841775 * a - 1.291485548 * b;
  const l3 = lPrime ** 3;
  const m3 = mPrime ** 3;
  const s3 = sPrime ** 3;
  const rLinear = 4.0767416621 * l3 - 3.3077115913 * m3 + 0.2309699292 * s3;
  const gLinear = -1.2684380046 * l3 + 2.6097574011 * m3 - 0.3413193965 * s3;
  const bLinear = -0.0041960863 * l3 - 0.7034186147 * m3 + 1.707614701 * s3;
  const encode = (channel) => {
    const clamped = Math.min(1, Math.max(0, channel));
    return clamped <= 0.0031308
      ? clamped * 12.92
      : 1.055 * clamped ** (1 / 2.4) - 0.055;
  };
  return {
    r: encode(rLinear),
    g: encode(gLinear),
    b: encode(bLinear),
    a: alpha,
  };
}

const DIGIT_WORDS = { 2: "two", 3: "three", 4: "four" };
const capitalize = (value) => value
  ? value[0].toUpperCase() + value.slice(1)
  : value;
const decapitalize = (value) => value
  ? value[0].toLowerCase() + value.slice(1)
  : value;

export function camelize(name) {
  return String(name)
    .split("-")
    .map((part, index) => {
      const match = index === 0 ? part.match(/^([234])(.*)$/) : null;
      return match
        ? DIGIT_WORDS[match[1]] + capitalize(match[2])
        : part;
    })
    .map((part, index) => index === 0 ? decapitalize(part) : capitalize(part))
    .join("");
}

const swiftName = (name) => ({
  background: "bg",
  surfaceAlt: "surface2",
  interactiveHover: "chatHover",
  interactiveActive: "chatActive",
  selected: "surface3",
  primaryPressed: "primaryPress",
}[name] ?? camelize(name));

const number = (value) => Number(Number(value).toFixed(6)).toString();

function swiftPair(pair) {
  const component = (value) => {
    const color = oklchToSrgb(value);
    return `ColorComponents(red: ${number(color.r)}, green: ${number(color.g)}, blue: ${number(color.b)}, alpha: ${number(color.a)})`;
  };
  return `ColorPair(light: ${component(pair.light)}, dark: ${component(pair.dark)})`;
}

const HEADER = `// Generated by design/tokens/generate-ios-tokens.mjs — do not edit.
// Source of truth: design/tokens/fish.tokens.json (extracted from apps/web/app/globals.css).
`;

export function renderColorSwift(manifest) {
  const lines = [HEADER, "import SwiftUI", "import UIKit", ""];
  lines.push(`public struct ColorComponents: Sendable, Equatable {
    public let red: Double
    public let green: Double
    public let blue: Double
    public let alpha: Double

    public init(red: Double, green: Double, blue: Double, alpha: Double) {
        self.red = red
        self.green = green
        self.blue = blue
        self.alpha = alpha
    }
}

public struct ColorPair: Sendable, Equatable {
    public let light: ColorComponents
    public let dark: ColorComponents

    public init(light: ColorComponents, dark: ColorComponents) {
        self.light = light
        self.dark = dark
    }

    public var color: Color {
        Color(UIColor { traits in
            let value = traits.userInterfaceStyle == .dark ? dark : light
            return UIColor(
                red: value.red,
                green: value.green,
                blue: value.blue,
                alpha: value.alpha
            )
        })
    }
}
`);

  const colors = Object.entries(manifest.color);
  const aliases = Object.entries(manifest.chat.colorAliases);
  lines.push("/// Raw light/dark values used by contrast tests.");
  lines.push("public enum ColorTokens {");
  for (const [name, pair] of colors) {
    lines.push(`    public static let ${swiftName(name)} = ${swiftPair(pair)}`);
  }
  for (const [name, target] of aliases) {
    lines.push(`    public static let ${swiftName(name)} = ${swiftName(target)}`);
  }
  lines.push("}", "", "/// Semantic SwiftUI colors. Light/dark resolve automatically.");
  lines.push("public enum Palette {");
  for (const [name] of colors) {
    lines.push(`    public static let ${swiftName(name)} = ColorTokens.${swiftName(name)}.color`);
  }
  for (const [name] of aliases) {
    lines.push(`    public static let ${swiftName(name)} = ColorTokens.${swiftName(name)}.color`);
  }
  lines.push("}", "");
  return lines.join("\n");
}

export function renderTokensSwift(manifest) {
  const lines = [HEADER, "import SwiftUI", ""];
  const numericEnum = (name, values, type, transform = (value) => value, doc = "") => {
    if (doc) lines.push(`/// ${doc}`);
    lines.push(`public enum ${name} {`);
    for (const [key, value] of Object.entries(values)) {
      lines.push(`    public static let ${camelize(key)}: ${type} = ${number(transform(value))}`);
    }
    lines.push("}", "");
  };

  numericEnum("Spacing", manifest.spacingDp, "CGFloat", undefined,
    "Spacing roles — use the role, never the number.");
  numericEnum("Radius", manifest.radiusDp, "CGFloat", undefined,
    "Corner radii by role.");
  numericEnum("Metrics", manifest.sizePt, "CGFloat", undefined,
    "iOS interaction targets and component dimensions in points.");
  numericEnum("AspectRatio", manifest.aspect, "CGFloat", undefined,
    "Media aspect ratios (width / height) shared with the web contract.");
  numericEnum("MotionDuration", manifest.durationMs, "TimeInterval", (value) => value / 1000,
    "Durations in seconds (the manifest stores milliseconds).");
  numericEnum("Opacity", manifest.opacity, "Double", undefined,
    "Focus opacities matching the shared visual contract.");

  lines.push("/// Fixed product type ladder. Typography maps these to fonts.");
  lines.push("public enum TypeScale {");
  for (const [role, size] of Object.entries(manifest.typeSp)) {
    lines.push(
      `    public static let ${camelize(role)} = TypeSpec(font: "${manifest.typeFont[role]}", weight: ${manifest.typeWeight[role]}, size: ${number(size)}, lineHeight: ${number(manifest.lineHeightMultiplier[role])})`,
    );
  }
  lines.push("}", "");
  lines.push(`public struct TypeSpec: Sendable, Equatable {
    public let font: String
    public let weight: Int
    public let size: CGFloat
    public let lineHeight: CGFloat

    public init(font: String, weight: Int, size: CGFloat, lineHeight: CGFloat) {
        self.font = font
        self.weight = weight
        self.size = size
        self.lineHeight = lineHeight
    }
}
`);
  lines.push("/// Chat interaction contract shared with web and Android.");
  lines.push("public enum ChatRules {");
  lines.push(`    public static let maxMessageLength: Int = ${manifest.chat.maxMessageLength}`);
  lines.push(`    public static let counterThreshold: Int = ${manifest.chat.counterThreshold}`);
  lines.push(`    public static let messageMaxWidthFraction: CGFloat = ${number(manifest.chat.messageMaxWidthFraction)}`);
  lines.push("}", "");
  return lines.join("\n");
}

function main() {
  const manifest = JSON.parse(readFileSync(MANIFEST, "utf8"));
  const outputs = {
    "Palette.generated.swift": renderColorSwift(manifest),
    "Tokens.generated.swift": renderTokensSwift(manifest),
  };
  const check = process.argv.includes("--check");
  mkdirSync(OUT_DIR, { recursive: true });
  let dirty = false;
  for (const [filename, content] of Object.entries(outputs)) {
    const target = path.join(OUT_DIR, filename);
    const current = existsSync(target) ? readFileSync(target, "utf8") : null;
    if (current === content) continue;
    if (check) {
      console.error(`[tokens] ${filename} is stale — run: pnpm ios:tokens`);
      dirty = true;
    } else {
      writeFileSync(target, content);
      console.log(`[tokens] wrote ${path.relative(process.cwd(), target)}`);
    }
  }
  if (check && dirty) process.exit(1);
  if (check) console.log("[tokens] generated Swift is up to date");
}

if (process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1])) {
  main();
}
