#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = path.join(root, "design/tokens/fish.tokens.json");
const outputPath = path.join(
  root,
  "apps/android/core/designsystem/src/main/kotlin/space/fishhub/android/core/designsystem/tokens/GeneratedTokens.kt",
);
const tokens = JSON.parse(fs.readFileSync(sourcePath, "utf8"));

const pascal = (value) => value.charAt(0).toUpperCase() + value.slice(1);
const decimals = (value) => Number(value).toFixed(4).replace(/0+$/, "").replace(/\.$/, "");
const kotlinDouble = (value) => {
  const result = decimals(value);
  return result.includes(".") ? result : `${result}.0`;
};
const dpObject = (name, values) => `internal object ${name} {\n${Object.entries(values)
  .map(([key, value]) => `    val ${pascal(key)} = ${decimals(value)}.dp`)
  .join("\n")}\n}`;
const intObject = (name, values) => `internal object ${name} {\n${Object.entries(values)
  .map(([key, value]) => `    const val ${pascal(key)} = ${Math.round(value)}`)
  .join("\n")}\n}`;

const colorValues = Object.entries(tokens.color)
  .map(([key, value]) => {
    const light = value.light.map(kotlinDouble).join(", ");
    const dark = value.dark.map(kotlinDouble).join(", ");
    return `    val ${pascal(key)} = GeneratedColorToken(oklch(${light}), oklch(${dark}))`;
  })
  .join("\n");

const typeValues = Object.entries(tokens.typeSp)
  .map(([key, value]) => {
    const multiplier = tokens.lineHeightMultiplier[key];
    return [
      `    val ${pascal(key)}Size = ${decimals(value)}.sp`,
      `    val ${pascal(key)}LineHeight = ${decimals(value * multiplier)}.sp`,
    ].join("\n");
  })
  .join("\n");

const output = `// Generated by scripts/generate-android-design-tokens.mjs. Do not edit by hand.
package space.fishhub.android.core.designsystem.tokens

import androidx.compose.ui.graphics.Color
import androidx.compose.ui.graphics.colorspace.ColorSpaces
import androidx.compose.ui.unit.dp
import androidx.compose.ui.unit.sp
import kotlin.math.PI
import kotlin.math.cos
import kotlin.math.sin

internal data class GeneratedColorToken(val light: Color, val dark: Color)

private fun oklch(lightness: Double, chroma: Double, hueDegrees: Double, alpha: Double): Color {
    val hueRadians = hueDegrees * PI / 180.0
    return Color(
        lightness.toFloat(),
        (chroma * cos(hueRadians)).toFloat(),
        (chroma * sin(hueRadians)).toFloat(),
        alpha.toFloat(),
        ColorSpaces.Oklab,
    )
}

internal object GeneratedColors {
${colorValues}
}

${dpObject("GeneratedSpacing", tokens.spacingDp)}

${dpObject("GeneratedRadii", tokens.radiusDp)}

${dpObject("GeneratedSizes", tokens.sizeDp)}

internal object GeneratedType {
${typeValues}
}

${intObject("GeneratedDurations", tokens.durationMs)}

internal object GeneratedLayout {
    const val MessageMaxWidthFraction = ${decimals(tokens.layout.messageMaxWidthFraction)}f
    val TwoPaneBreakpoint = ${decimals(tokens.layout.twoPaneBreakpointDp)}.dp
}
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, output);
console.log(path.relative(root, outputPath));
